	<p>
		A customer needs a plumber. They open ChatGPT and type, "Who is the best plumber in Tampa for a
		burst pipe?"
	</p>

	<p>The answer is one business. Sometimes two. Not ten blue links. Not a map pack.</p>

	<p>If that business is not yours, you do not get the call.</p>

	<p>
		This is the question every local business owner should be asking right now: how do you become
		the answer instead of a footnote.
	</p>

	<h2>How AI search actually picks</h2>

	<p>
		ChatGPT, Perplexity, and Google AI are not crawling the web the way Google did for 20 years.
		They are reading sources, summarizing what is consistent across them, and picking what to
		surface.
	</p>

	<p>Three things drive the pick:</p>

	<ul>
		<li>
			<strong>Clarity.</strong> The page says what the business does, who it serves, and where, in
			one read.
		</li>
		<li>
			<strong>Consistency.</strong> The business name, address, phone, and services match across
			the site, Google Business Profile, directories, and review sites.
		</li>
		<li>
			<strong>Trust signals.</strong> Real reviews, real photos, real owner names, real addresses.
			Not stock content.
		</li>
	</ul>

	<p>The businesses that win these three are the ones AI recommends. The rest get skipped.</p>

	<h2>What gets a local business skipped</h2>

	<p>The patterns we see most:</p>

	<ul>
		<li>Vague service descriptions that could apply to any business in the category</li>
		<li>No mention of the actual city or neighborhood served</li>
		<li>A homepage that lists everything instead of saying one thing well</li>
		<li>Stock photos and generic copy that read like a template</li>
		<li>No clear next step on the page</li>
		<li>Outdated hours, addresses, or service lists across the web</li>
	</ul>

	<p>
		If an AI cannot summarize what you do in two sentences, it is not going to recommend you in
		two sentences either.
	</p>

	<h2>The five fixes that move the needle</h2>

	<p>
		These are not theoretical. They are what we see produce results when we work with local
		service businesses.
	</p>

	<h3>1. Say what you do, where, in plain language</h3>

	<p>
		The first sentence of your homepage should answer three questions: what trade or service,
		which city or neighborhood, and one trust signal.
	</p>

	<p>
		Weak: "Premium plumbing solutions for discerning homeowners."
	</p>

	<p>
		Strong: "Licensed emergency plumbers serving Tampa and St. Pete since 2008. Same-day calls
		answered."
	</p>

	<h3>2. Fix your Google Business Profile</h3>

	<p>
		AI tools read your Google Business Profile. If the categories are wrong, the hours are stale,
		or the service area is missing, you are invisible to the systems doing the recommending.
	</p>

	<p>
		Check the category. Check the hours. Add real photos. Reply to recent reviews. Update at least
		once a month.
	</p>

	<h3>3. Match your name, address, and phone everywhere</h3>

	<p>
		If your website says "Smith Plumbing" and your directory listings say "Smith Plumbing Co"
		and "Smith Plumbing Tampa," AI tools see three businesses, not one.
	</p>

	<p>Pick one version. Use it everywhere. Yelp, BBB, Facebook, Nextdoor, the works.</p>

	<h3>4. Get real reviews about specific things</h3>

	<p>
		"Great service!" reviews do not help AI summarize what you are good at. Reviews that mention
		the actual job — "fixed our water heater leak on a Sunday in three hours" — give AI specific
		facts to cite.
	</p>

	<p>Ask happy customers to mention the service, the city, and the outcome.</p>

	<h3>5. Add one clear next step on every page</h3>

	<p>
		Most local sites bury the phone number or the contact form. AI tools that summarize a page
		need to surface a way to contact you. Put one obvious call-to-action above the fold and on
		every page.
	</p>

	<h2>What about traditional SEO?</h2>

	<p>It still matters. AI search and traditional search are not separate. They feed each other.</p>

	<p>
		A page that ranks well on Google because it is clear, fast, and helpful is also a page that
		AI summarizers cite first. Good local SEO is now table stakes for AI visibility.
	</p>

	<p>
		The difference is the bar. Five years ago, you could rank by stuffing service area pages with
		city names. That does not work anymore. AI tools penalize the same thin content that humans
		ignore.
	</p>

	<h2>How long does this take to work</h2>

	<p>
		The Google Business Profile fixes show up in days. Site clarity and review signals build over
		weeks. Full AI visibility for competitive local terms takes months.
	</p>

	<p>The businesses that start now have a head start on the ones that wait.</p>

	<h2>The takeaway</h2>

	<p>
		AI search is not a separate channel to chase. It is the same job local businesses have always
		had — being easy to find, easy to understand, easy to trust — done with new tools watching.
	</p>

	<p>
		Fix the basics. Be specific. Be consistent. The recommendation engines will follow.
	</p>
