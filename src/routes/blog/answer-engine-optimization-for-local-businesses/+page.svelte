	</p>

	<p>
		<strong>If you have nothing else,</strong> rewrite the first 200 words of your homepage. State
		the service, the city, one credential, one response promise. Read it aloud. If a stranger
		cannot repeat what you do after one read, rewrite again.
	</p>

	<p>
		<strong>If your homepage is solid,</strong> spend 30 minutes on your Google Business Profile.
