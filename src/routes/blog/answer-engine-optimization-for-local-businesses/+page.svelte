	<p>
		Answer Engine Optimization, or AEO, is how a local business gets recommended when a customer
		types a full question into ChatGPT, Perplexity, or Google's AI Overview.
	</p>

	<p>
		That is the whole definition. The rest of this guide is what to actually do about it.
	</p>

	<p>
		If you run a plumbing, dental, legal, or any local service business, the playbook here is
		written for you. No theory. No "AI revolution" framing. The five things AI tools read, the
		five things they ignore, and the order to fix them in.
	</p>

	<h2>What AEO is, and what it is not</h2>

	<p>
		Old search worked like a directory. A customer typed "plumber Tampa," Google returned ten blue
		links, the customer clicked through and decided who to call. Your job in SEO was to be in those
		ten links.
	</p>

	<p>
		New search is different. A customer asks ChatGPT: "Who is the best emergency plumber in Tampa
		for a burst pipe at 9pm on a Saturday?" The answer is one business. Sometimes two. Not ten
		links. Not a map. A specific recommendation with a name, a phone number, and a sentence about
		why.
	</p>

	<p>
		AEO is the work of becoming that specific recommendation.
	</p>

	<p>
		AEO is not a separate channel. It rides on top of the same fundamentals that have always
		mattered: a clear website, consistent business information across the internet, real reviews
		from real customers. What changed is the standard. Vague is now disqualifying. "Premium
		plumbing solutions for discerning homeowners" used to rank. It now reads as fluff that an AI
		cannot summarize, so the AI picks a competitor it can.
	</p>

	<h2>Why AI search picks one business and not another</h2>

	<p>
		AI search engines do not crawl the web the way Google did for the last twenty years. They read
		sources, find what is consistent across them, and surface the answer that is easiest to defend.
	</p>

	<p>Three signals drive every pick:</p>

	<p>
		<strong>Clarity.</strong> The business page says what it does, who it serves, and where, in
		one read. An AI can summarize the offer in a single sentence without inventing anything.
	</p>

	<p>
		<strong>Consistency.</strong> The business name, address, phone number, services, and hours
		match across the website, Google Business Profile, Yelp, Facebook, industry directories, and
		review sites. When the AI cross-checks, nothing contradicts.
	</p>

	<p>
		<strong>Trust signals.</strong> Reviews mention the specific service and the specific outcome.
		Photos look like the actual location, not stock. Owner names, license numbers, and years in
		business are present and findable.
	</p>

	<p>
		The businesses that win these three are the ones AI tools recommend. Everyone else gets
		skipped.
	</p>

	<h2>The five things AI tools read</h2>

	<h3>1. The first 200 words of your homepage</h3>

	<p>
		If the AI cannot tell what you do, where, and one trust signal in the opening section, you are
		already out. The opening should answer: what service, what city or neighborhood, what makes
		you the obvious call.
	</p>

	<p>
		Weak: "We provide a full suite of premium services to a diverse clientele."
	</p>

	<p>
		Strong: "Licensed emergency plumbers serving Tampa and St. Petersburg since 2008. Same-day
		service. Most calls answered in under 60 seconds."
	</p>

	<h3>2. Your Google Business Profile</h3>

	<p>
		AI tools read your Google Business Profile directly. If the categories are wrong, the hours are
		stale, the service area is missing, or there are no photos, you are invisible to the
		recommendation engine.
	</p>

	<p>
		Check the primary category. Add every secondary category that applies. Update hours seasonally.
		Add at least 12 real photos of the location, the team, and recent work. Reply to recent reviews
		within 48 hours.
	</p>

	<h3>3. Your reviews, line by line</h3>

	<p>
		Five-star "great service" reviews do not help AI summarize what you are good at. They are
		flat. AI tools cite the reviews that mention the specific service and the specific outcome.
	</p>

	<p>
		Weak review: "Loved them, will use again." Cannot be cited.
	</p>

	<p>
		Strong review: "Fixed our water heater leak on a Sunday afternoon, on-site within two hours,
		flat fee quoted before the work started." Can be quoted directly by an AI assistant.
	</p>

	<p>
		After every job, ask the customer to mention the service performed, the city, and the outcome.
		A simple email with three short prompts works better than a generic "please leave us a review."
	</p>

	<h3>4. Consistent name, address, and phone across the internet</h3>

	<p>
		If your website says "Smith Plumbing," your Yelp says "Smith Plumbing Co," and your BBB says
		"Smith Plumbing Tampa," AI tools see three businesses. Pick one version of the name and use it
		everywhere: Google Business Profile, Yelp, Facebook, Nextdoor, Angie's List, Yellow Pages,
		industry-specific directories.
	</p>

	<p>
		Same goes for the phone number and address. One canonical version. Updated everywhere within
		30 days when anything changes.
	</p>

	<h3>5. Clear next steps on every page</h3>

	<p>
		AI tools that summarize a page need a way to point the customer toward action. Bury the phone
		number and the contact form and the AI has nothing to recommend. Put one obvious call to
		action above the fold and a clear contact section in the footer of every page.
	</p>

	<h2>The five things AI tools ignore</h2>

	<p>
		Most of what local service business owners are told to do for "AI optimization" is wasted
		effort. AI tools do not care about:
	</p>

	<p>
		<strong>Keyword density.</strong> Stuffing "plumber Tampa" into every paragraph hurts more than
		it helps. AI tools reward natural language.
	</p>

	<p>
		<strong>Meta keywords.</strong> Dead since 2009. AI tools do not read them.
	</p>

	<p>
		<strong>Schema markup as a magic fix.</strong> Schema is useful but secondary. If your homepage
		copy is vague, schema will not save you. Fix the copy first.
	</p>

	<p>
		<strong>Generic blog content.</strong> A "10 reasons to call a plumber" article written by an AI
		will rank for nothing and get cited by no one. AI tools penalize the same thin content humans
		ignore.
	</p>

	<p>
		<strong>Stock photos.</strong> AI tools do not directly penalize them, but real photos
		correlate with the other trust signals that matter. Stock photos signal a business is not
		invested in its own presence.
	</p>

	<h2>How AEO is different by trade</h2>

	<h3>Plumbers and HVAC</h3>

	<p>
		Emergency intent dominates. "Burst pipe near me," "no AC on Saturday," "water heater leaking."
		AI tools that surface plumbers prioritize three things: speed of response, service area
		precision, and recent reviews mentioning emergency calls. If your homepage does not promise a
		response time and your reviews do not mention emergency work, you will lose to a competitor
		whose do.
	</p>

	<h3>Dentists and medical practices</h3>

	<p>
		Trust intent dominates. "Family dentist near me," "best dentist for anxious patients,"
		"pediatric dentist accepting new patients." AI tools surface dentists whose pages name the
		specific services (sedation, pediatric, cosmetic), accept new patients explicitly, and have
		reviews mentioning the specific service category. Insurance accepted matters. Years in
		practice matters. License numbers and credentials matter.
	</p>

	<h3>Lawyers</h3>

	<p>
		Specificity wins. "Personal injury lawyer Tampa," "estate planning attorney near me,"
		"DUI defense Hillsborough County." AI tools heavily favor lawyers whose pages name the specific
		practice area, county or jurisdiction served, and case-result language ("recovered," "verdict,"
		"settlement"). Generic "full-service law firm" pages get skipped.
	</p>

	<h3>Med spas, salons, real estate, accounting</h3>

	<p>
		Visual and credibility intent. AI tools favor businesses with real photos of work, named
		providers (the actual stylist, agent, or accountant doing the work), and reviews that mention
		the specific service and outcome. Anonymous corporate-style sites lose to single-page sites
		with a real face and a phone number.
	</p>

	<h2>SEO is not dead. It is the floor.</h2>

	<p>
		Some marketing voices have declared SEO obsolete. That is wrong.
	</p>

	<p>
		AI search and traditional search feed each other. A page that ranks well on Google because it
		is clear, fast, and authoritative is also the page AI tools cite first. Good local SEO is now
		the floor for AI visibility, not a separate game.
	</p>

	<p>
		The difference is the bar. Five years ago you could rank by stuffing service area pages with
		city names. That technique now produces pages an AI cannot summarize, and they get
		deprioritized in both Google and AI search. The new floor is: every page passes the "could a
		stranger explain what this business does after one read" test.
	</p>

	<h2>How to measure if AEO is working</h2>

	<p>
		Three metrics, in order of importance.
	</p>

	<p>
		<strong>One. Brand mentions in AI search results.</strong> Tools like Ahrefs Brand Radar,
		Perplexity Analytics, and direct prompt testing show whether your business is being cited.
		Start with the prompts a real customer would type. If you are not surfacing on those, no other
		metric matters yet.
	</p>

	<p>
		<strong>Two. Direct traffic with intent signals.</strong> AI search tools often deliver
		visitors who type the URL directly after seeing a recommendation. Watch direct traffic in
		Google Analytics with session duration over two minutes. Those are the visitors who heard your
		business name from an AI and came to verify.
	</p>

	<p>
		<strong>Three. Phone calls and form fills from new sources.</strong> Ask new customers how they
		found you. "ChatGPT told me about you" went from a joke in 2024 to a meaningful percentage in
		some markets by 2026. If you are not asking, you are not measuring.
	</p>

