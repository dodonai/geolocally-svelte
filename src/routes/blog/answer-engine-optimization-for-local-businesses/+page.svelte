	<p>
		AEO is not a new field requiring new specialists. It is the same job local service businesses
		have always had — being easy to find, easy to understand, easy to trust — done with new
		readers paying attention.
	</p>

	<p>
		Fix the fundamentals. Be specific. Be consistent. Get real reviews about real work. The
		recommendation engines will follow.
